/*
number -> Representa un valor numérico
string -> Representa una cadena de caracteres
boolean -> Representa un valor booleano, verdadero o falso
any -> Representa cualquier valor
void -> Se usan en el contexto de las funciones para determinar que la función no retorna ningun valor.
null -> Representa un valor nulo
undefined -> Representa un valor indefinido
Array<T> o T[] o [] -> Representa una lista
enum -> Permite definir un conjunto de constantes , como especie de lista.
class -> Define estructuras y comportamientos de objetos de manera mas estructura, se puede decir que una clase podría ser un objeto
interfaces -> Una interface define un contrato en el código, se cumple todo o no se cumple nada, especifican que propiedades y métodos debe tener un objeto.
function -> Representa una función.
partial -> convierte todas las propiedades en opcionales
 */